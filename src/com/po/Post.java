package com.po;

public class Post {

	private String userName;
	private String postTitle;
	private String postContent;
	private String postTime;
	private String readingsNumber;
	private String commentsNumber;
	private String goodsNumber;
	private String wordsNumber;

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getPostTitle() {
		return postTitle;
	}

	public void setPostTitle(String postTitle) {
		this.postTitle = postTitle;
	}

	public String getPostContent() {
		return postContent;
	}

	public void setPostContent(String postContent) {
		this.postContent = postContent;
	}

	public String getPostTime() {
		return postTime;
	}

	public void setPostTime(String postTime) {
		this.postTime = postTime;
	}

	public String getReadingsNumber() {
		return readingsNumber;
	}

	public void setReadingsNumber(String readingsNumber) {
		this.readingsNumber = readingsNumber;
	}

	public String getCommentsNumber() {
		return commentsNumber;
	}

	public void setCommentsNumber(String commentsNumber) {
		this.commentsNumber = commentsNumber;
	}

	public String getGoodsNumber() {
		return goodsNumber;
	}

	public void setGoodsNumber(String goodsNumber) {
		this.goodsNumber = goodsNumber;
	}

	public String getWordsNumber() {
		return wordsNumber;
	}

	public void setWordsNumber(String wordsNumber) {
		this.wordsNumber = wordsNumber;
	}
}
